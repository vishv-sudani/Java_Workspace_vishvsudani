package Work_26_7;

public class Switchcase {

	public static void main(String[] args) {
		
		int num=5;
		
		switch(num)
		{
		case 1:System.out.println("English");
		break;
		
		case 2:System.out.println("Hindi");
		break;
		
		case 3:System.out.println("Gujarati");
		break;
		
		default:System.out.println("Error");
		break;
		}
	}
}
